// Primitive Data Types in JavaScript

// Integer: A whole number, either positive or negative.
let age = 25;
console.log(`Integer: ${age}, Type: ${typeof age}`);

// Float: A number with decimal points (real numbers).
let circleRadius = 3.1416;
console.log(`Float: ${circleRadius}, Type: ${typeof circleRadius}`);

// String: A sequence of characters enclosed in quotes.
let userName = "Md Abdul Ahad Linkon";
console.log(`String: ${userName}, Type: ${typeof userName}`);

// Boolean: Represents a true or false value.
let isLoggedIn = true;
console.log(`Boolean: ${isLoggedIn}, Type: ${typeof isLoggedIn}`);
